package modelo;

public class Fornecedor {
	private int idFor;
	private String nomeFor, telefoneFor, emailFor;
	public int getIdFor() {
		return idFor;
	}
	public void setIdFor(int idFor) {
		this.idFor = idFor;
	}
	public String getNomeFor() {
		return nomeFor;
	}
	public void setNomeFor(String nomeFor) {
		this.nomeFor = nomeFor;
	}
	public String getTelefoneFor() {
		return telefoneFor;
	}
	public void setTelefoneFor(String telefoneFor) {
		this.telefoneFor = telefoneFor;
	}
	public String getEmailFor() {
		return emailFor;
	}
	public void setEmailFor(String emailFor) {
		this.emailFor = emailFor;
	}
}
